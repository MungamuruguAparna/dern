package com.derniercri.customer.form.registration;

public class CustomerRegistrationForm {

	protected String emailAddress;
	protected String mobileNo;
	protected String password;
	protected String reTypePassword;
	protected String termAndConditions;
	public String getEmailAddress() {
		return emailAddress;
	}
	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}
	public String getMobileNo() {
		return mobileNo;
	}
	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getReTypePassword() {
		return reTypePassword;
	}
	public void setReTypePassword(String reTypePassword) {
		this.reTypePassword = reTypePassword;
	}
	public String getTermAndConditions() {
		return termAndConditions;
	}
	public void setTermAndConditions(String termAndConditions) {
		this.termAndConditions = termAndConditions;
	}
	
	
	
}
